//función usada para conectarse al docker
void reconnect() {

	while (!client.connected()) {
		Serial.print("Intentando conexión Mqtt...");
		// Creamos un cliente ID
		String clientId = "IOTICOS_H_W_" ;
		clientId += String(random(0xffff), HEX);
		// Intentamos conectar
		if (client.connect(clientId.c_str(),mqtt_user,mqtt_pass)) {
			Serial.println("Conectado!");
			// Nos suscribimos
			if(client.subscribe(root_topic_subscribe)){
        		Serial.println("Suscripcion ok");
      }else{
        Serial.println("fallo Suscripciión");
      }
		} else {
			Serial.print("falló :( con error -> ");
			Serial.print(client.state());
			Serial.println(" Intentamos de nuevo en 5 segundos");
		}
	}
}

//función usada al recibir un mensaje
//al llegar un mensaje se iguala a la variable que corresponde segun su prefijo
void callback(char* topic, byte* payload, unsigned int length){
	String incoming = "";
	Serial.print("Mensaje recibido desde -> ");
	Serial.print(topic);
	Serial.println("");
	for (int i = 0; i < length; i++) {
		incoming += (char)payload[i];
	}
	incoming.trim();

    if(incoming[0] == 't'){//tirmpo
		incoming[0] = ' ';
    	myTime = incoming;
  	}else if(incoming[0] == 'd'){//fecha
    	incoming[0] = ' ';
    	date = incoming;
  	}else if(incoming[0] == 'i'){//selección imagen
    	imagen = incoming[1];
  	}else if(incoming[0] == 'm'){//mensaje de texto
		incoming[0] = ' ';
    	mensaje = incoming;
  	}else if(incoming[0] == 'p' && incoming[1] == 'm'){ //movimiento del adversario(no se usa)
		incoming[0] = ' ';
		incoming[1] = ' ';
		move = incoming;
  	}


	//este fragmento enciende el led 2(azul) por los botones de la pagina 
	if(incoming == "0"){
		digitalWrite(BUILTIN_LED,LOW);
		Serial.println("\n led apagado");	
	}else if(incoming == "1"){
		digitalWrite(BUILTIN_LED,HIGH);
		Serial.println("\n led prendido");	
	}	
	

	Serial.println("Mensaje -> " + incoming);

}
